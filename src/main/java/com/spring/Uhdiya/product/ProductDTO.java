package com.spring.Uhdiya.product;

public class ProductDTO {
	
	
	private int product_id;
	private String product_code;
	private String product_name;
	private String product_cateL;
	private String product_cateS;
	private int product_price;
	
	private String product_fileName;
	
	
	
	
	public String getProduct_fileName() {
		return product_fileName;
	}



	public void setProduct_fileName(String product_fileName) {
		this.product_fileName = product_fileName;
	}




	
	

	public int getProduct_id() {
		return product_id;
	}



	public void setProduct_id(int product_id) {
		this.product_id = product_id;
	}



	public String getProduct_code() {
		return product_code;
	}
	public void setProduct_code(String product_code) {
		this.product_code = product_code;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public String getProduct_cateL() {
		return product_cateL;
	}
	public void setProduct_cateL(String product_cateL) {
		this.product_cateL = product_cateL;
	}
	public String getProduct_cateS() {
		return product_cateS;
	}
	public void setProduct_cateS(String product_cateS) {
		this.product_cateS = product_cateS;
	}
	public int getProduct_price() {
		return product_price;
	}
	public void setProduct_price(int product_price) {
		this.product_price = product_price;
	}
	
	
	
	
}
